enum VGA_State {
	H_FRONT_PORCH = 0,
	H_SYNC_PULSE,
	H_BACK_PORCH,
	V_FRONT_PORCH,
	V_SYNC_PULSE,
	V_BACK_PORCH,
	SHOW_PIXELS
};

#define CWIDTH 50
#define CHEIGHT 30

#define SHOW_PIXELS_T 640
#define H_FRONT_PORCH_T 56
#define H_SYNC_PULSE_T 56
#define H_BACK_PORCH_T 80

#define V_FRONT_PORCH_T (1*832)
#define V_SYNC_PULSE_T (3*832)
#define V_BACK_PORCH_T (25*832)

#define TICK(N) for(int i = 0;i < N;i++) __asm__("nop")

#define timerSet(S) timer_set_counter(TIM2, (S-50))
#define GPIO_VSYNC GPIO1
#define GPIO_HSYNC GPIO2

void draw(uint8_t*, int);
void renderLine(uint8_t*, int);

uint8_t line1[CWIDTH], line2[CWIDTH];
char cbuffer[CWIDTH*CHEIGHT];
int state;
int lineCounter = 0;


void update(){
	if(lineCounter == 0){
		gpio_clear(GPIOA, GPIO_VSYNC);
		renderLine(line1, 0);
	}
	else if(lineCounter == 2)
		gpio_set(GPIOA, GPIO_VSYNC);
	else if(lineCounter >= 31 && lineCounter <= 510){
		gpio_clear(GPIOA, GPIO_HSYNC);
		TICK(50);
		gpio_set(GPIOA, GPIO_HSYNC);
		TICK(11);
		int l = lineCounter - 31;
		draw(l & 0b10 ? line2 : line1, CWIDTH);
		if(!(l & 0b1))
			renderLine(l & 0b10 ? line1 : line2, l >> 2);

 	}
	else if(lineCounter == 521){
		lineCounter = 0;
		return;
	}
	lineCounter++;
}

void tim2_isr(){
	if(timer_get_flag(TIM2, TIM_SR_UIF)){
		timer_clear_flag(TIM2, TIM_SR_UIF);
		update();
	}
}

void VGA_init(){
	timerSet(V_BACK_PORCH_T);
	state = V_BACK_PORCH;
	for(int i = 0;i < CWIDTH*CHEIGHT;i++)
		cbuffer[i] = ' ';

	cbuffer[0] = 'A';
	rcc_periph_clock_enable(RCC_SPI1);
	rcc_periph_clock_enable(RCC_DMA1);

	spi_enable_software_slave_management(SPI1);
	spi_set_nss_high(SPI1);

	gpio_set_mode(GPIOA, GPIO_MODE_OUTPUT_50_MHZ, GPIO_CNF_OUTPUT_ALTFN_PUSHPULL, GPIO_SPI1_MOSI | GPIO_SPI1_SCK);
	gpio_set_mode(GPIOA, GPIO_MODE_OUTPUT_50_MHZ, GPIO_CNF_OUTPUT_PUSHPULL, GPIO1 | GPIO2 | GPIO13);
	gpio_set_mode(GPIOC, GPIO_MODE_OUTPUT_50_MHZ, GPIO_CNF_OUTPUT_PUSHPULL, GPIO13);
	gpio_set(GPIOA, GPIO1 | GPIO2);

	spi_init_master(
			SPI1,
			SPI_CR1_BAUDRATE_FPCLK_DIV_4,
			SPI_CR1_CPOL_CLK_TO_0_WHEN_IDLE,
            SPI_CR1_CPHA_CLK_TRANSITION_1,
			SPI_CR1_DFF_8BIT,
			SPI_CR1_LSBFIRST
	);

	spi_enable(SPI1);

	nvic_enable_irq(NVIC_TIM2_IRQ);


	rcc_periph_clock_enable(RCC_TIM2);
	timer_set_mode(TIM2, TIM_CR1_CKD_CK_INT,TIM_CR1_CMS_EDGE, TIM_CR1_DIR_DOWN);
	timer_set_prescaler(TIM2, 0);
	timer_enable_preload(TIM2);
	timer_set_period(TIM2, 2303);
	timer_enable_irq(TIM2, TIM_DIER_UIE);
	timer_enable_counter(TIM2);

}

void renderLine(uint8_t* lineData, int line){
	int cline8 = (line >> 3);
	int cinline = line & 0b111;
	for(int i = 0;i < CWIDTH;i++)
		lineData[i] = font8x8_basic[(int)cbuffer[cline8*CWIDTH+i]][cinline];

}

void draw(uint8_t* line, int len){
	dma_channel_reset(DMA1, DMA_CHANNEL3);
	dma_disable_peripheral_increment_mode(DMA1, DMA_CHANNEL3);
	dma_enable_memory_increment_mode(DMA1, DMA_CHANNEL3);
	dma_set_memory_address(DMA1, DMA_CHANNEL3, (uint32_t) line);
	dma_set_number_of_data(DMA1, DMA_CHANNEL3, len);
	dma_set_read_from_memory(DMA1, DMA_CHANNEL3);
	dma_set_peripheral_address(DMA1, DMA_CHANNEL3, (uint32_t)&SPI1_DR);
	dma_set_memory_size(DMA1, DMA_CHANNEL3, DMA_CCR_MSIZE_8BIT);
	dma_set_priority(DMA1, DMA_CHANNEL3, DMA_CCR_PL_HIGH);
	dma_enable_channel(DMA1, DMA_CHANNEL3);
	spi_enable_tx_dma(SPI1);
}


